    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        User user;
        if (idTxt.getText().length() > 0 & nameTxt.getText().length() > 0 & subNameTxt.getText().length() > 0 & tellTxt.getText().length() > 0 & mailTxt.getText().length() > 0) {
            user = new User(Integer.parseInt(idTxt.getText()), nameTxt.getText(), subNameTxt.getText(), tellTxt.getText(), mailTxt.getText());
            Dao.EditarCliente(user);

        } else if (nameTxt.getText().length() > 0 & subNameTxt.getText().length() > 0 & tellTxt.getText().length() > 0 & mailTxt.getText().length() > 0) {
            user = new User(0, nameTxt.getText(), subNameTxt.getText(), tellTxt.getText(), mailTxt.getText());
            Dao.AgregarCliente(user);

        } else {
            JOptionPane.showMessageDialog(null, "Campo de Texto Vacio.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        LlenarTable(tableClass);
        limpiarCampos();

    }//GEN-LAST:event_btnSaveMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked

        if (tableClass.getSelectedRow() > -1) {
            int input = javax.swing.JOptionPane.showConfirmDialog(null,
                    "Estas seguro que desea eliminar el usuario seleccionado?", "Eliminar Articulo", javax.swing.JOptionPane.YES_NO_OPTION);
            if (input == 0) {//yes
                int userId = lista.get(tableClass.getSelectedRow()).getId();
                Dao.EliminarCliente(userId);
                limpiarCampos();
                LlenarTable(tableClass);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No Selecciono ningun elemento.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnDeleteMouseClicked

