    private void tableClassMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableClassMouseClicked
        if (tableClass.getSelectedRow() > -1) {
            userId = lista.get(tableClass.getSelectedRow()).getId();
            userName = lista.get(tableClass.getSelectedRow()).getName();
            userSubname = lista.get(tableClass.getSelectedRow()).getSubname();
            userTell = lista.get(tableClass.getSelectedRow()).getTell();
            userEmail = lista.get(tableClass.getSelectedRow()).getEmail();

            idTxt.setText(Integer.toString(userId));
            nameTxt.setText(userName);
            subNameTxt.setText(userSubname);
            tellTxt.setText(userTell);
            mailTxt.setText(userEmail);
        }
    }//GEN-LAST:event_tableClassMouseClicked

    private void ClearTable(javax.swing.JTable tb) {
        javax.swing.table.DefaultTableModel dm = (javax.swing.table.DefaultTableModel) (tb.getModel());
        dm.setNumRows(0);
        tb.setModel(dm);
    }

    private void LlenarTable(javax.swing.JTable tb) {
        lista = Dao.getListUsers();
        ClearTable(tb);
        javax.swing.table.DefaultTableModel dm = (javax.swing.table.DefaultTableModel) (tb.getModel());
        if (!lista.isEmpty()) {
            for (int i = 0; i < lista.size(); i++) {

                dm.addRow(lista.get(i).getRow());
            }
        }
        tb.setModel(dm);

    }

    private void limpiarCampos() {
        idTxt.setText("");
        nameTxt.setText("");
        subNameTxt.setText("");
        tellTxt.setText("");
        mailTxt.setText("");
    }
